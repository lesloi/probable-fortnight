    private void jSupprimerActionPerformed(java.awt.event.ActionEvent evt) {
        
        if(this.listeStage.getSelectedIndex() != 0 && !this.jEffacer.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null, "Offre de stage supprimée.", "Information", JOptionPane.INFORMATION_MESSAGE);
            
            
            this.tabOffre.remove(this.listeStage.getSelectedIndex());
            
            this.jEffacerIntitule.setText("");
            this.jEffacerDescription.setText("");
            
            this.actualiserListeStage();
        }
    }
    
    private void jEffacerActionPerformed(java.awt.event.ActionEvent evt) {
        
        this.jAjoutIntitule.setText("");
        this.jAjoutDescription.setText("");
        
    }
    
    private void jEnvoyerActionPerformed(java.awt.event.ActionEvent evt) {
        
        int s = this.tabOffre.size();
        this.tabOffre.add(new Stage(s, this.jAjoutIntitule.getText(), this.jAjoutDescription.getText(), new Entreprise()));
        
        this.jAjoutIntitule.setText("");
        this.jAjoutDescription.setText("");
        
        actualiserListeStage();
        
    }
    
    private void listeConventionActionPerformed(java.awt.event.ActionEvent evt) {
        
        if(this.listeConvention.getSelectedIndex()!=0){
            this.jPrenomEtu.setText(this.getTabConv().get(listeConvention.getSelectedIndex()).getObj_etudiant().getAttr_str_prenom());
            this.jNomEtu.setText(this.getTabConv().get(listeConvention.getSelectedIndex()).getObj_etudiant().getAttr_str_nom());
            this.jIntituleConv.setText(this.getObj_entreprise().getAttr_list_catalogue().get(this.listeConvention.getSelectedIndex()).getAttr_str_intitule());
            this.jDescConv.setText(this.getObj_entreprise().getAttr_list_catalogue().get(this.listeConvention.getSelectedIndex()).getAttr_str_descOS());
        }
        
    }
    
