    private void listeStageActionPerformed(java.awt.event.ActionEvent evt) {
        
        int index = this.listeStage.getSelectedIndex();
        //Intitule:
        this.jEffacerIntitule.setText(this.tabOffre.get(index).getAttr_str_intitule());
        //Description:
        this.jEffacerDescription.setText(this.tabOffre.get(index).getAttr_str_descOS());
        
    }

    private void actualiserListeStage(){
        listeStage.removeAll();
        listeStage.add("LES STAGES",0);
        for(int i=1; i< this.tabOffre.size(); i++)
        {
            listeStage.add(tabOffre.get(i).getAttr_str_intitule());
        }

        this.listeStage.select(0);
    };
    
